void PrintImage(int[,] image) // структура массива
{
    for (int i = 0; i < image.GetLength(0); i++) // внешний цикл, щелкающие строки
    {
        for (int j = 0; j < image.GetLength(1); j++) // внутренний цикл, щелкающие столбцы
        {


            //Console.Write($"{image[i, j]} ");
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");

        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col);
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);

    }
}

